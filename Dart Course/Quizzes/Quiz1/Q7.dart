  int score = 89;

  if(score >= 90){
    print("Grade: A");
  } else if(score>=80 && score<=89){
    print("Grade: B");
  } else if(score>=70 && score<=79){
    print("Grade: C");
  } else {
    print("Grade: F");
  }
}







